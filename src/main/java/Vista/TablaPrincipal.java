    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        modelo = (DefaultTableModel) tablaTrabajadores.getModel();
        
        int filas = modelo.getRowCount();
        int presupuesto =0;
        int contador=0;
        for (int i = 0; i < filas; i++) {
            String estado = (String) modelo.getValueAt(i, 6);
            if (estado.equals("Activo")) {
                int salario = Integer.parseInt(String.valueOf(modelo.getValueAt(i, 5)));
                presupuesto+=salario;
                contador+=1;
            }
        }
        int duracion = Integer.parseInt(this.duracion.getText());
        this.presupuesto.setText("Para realizar la obra con "+contador+" trabajadores en "+duracion+" dias PPTotal = $ "+String.valueOf(presupuesto*duracion));
        //se calcula el costo total de la obra
    }//GEN-LAST:event_jButton1ActionPerformed


