    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        modelo = (DefaultTableModel) tablaTrabajadores.getModel();
        String nom = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 0);
        int filaSeleccionada = tablaTrabajadores.getSelectedRow(); //se pincha la fila del trabajador a eliminar
        int respuesta = JOptionPane.showConfirmDialog(this, "Desea borrar a: " + nom + "?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {
            modelo.removeRow(filaSeleccionada);
        } else {
            System.out.println("no acepto");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        modelo = (DefaultTableModel) tablaTrabajadores.getModel();
        String nombre = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 0);
        String apellido = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 1);
        String rut = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 2);
        int telefono = Integer.parseInt(String.valueOf(modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 3)));
        String rol = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 4);
        int salario = Integer.parseInt(String.valueOf( modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 5)));
        String estado = (String) modelo.getValueAt(tablaTrabajadores.getSelectedRow(), 6);
        int filaSeleccionada = tablaTrabajadores.getSelectedRow();
        //se actulizan los datos de cada trabajador
        empleado = new Empleado(nombre, apellido, rut, telefono, rol, salario, estado);
        Actualizar actualizar = new Actualizar(empleado, filaSeleccionada, this.getTablaTrabajadores());
        actualizar.setLocationRelativeTo(null);
        actualizar.setVisible(true);
        //this.setVisible(false);
        
    }//GEN-LAST:event_btnActualizarActionPerformed

